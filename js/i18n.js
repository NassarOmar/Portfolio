let currentLang = localStorage.getItem('lang') || 'en';

function t(key) {
  const keys = key.split('.');
  let val = translations[currentLang];
  for (const k of keys) {
    val = val?.[k];
  }
  if (val === undefined) {
    val = translations.en;
    for (const k of keys) val = val?.[k];
  }
  return val ?? key;
}

function setLanguage(lang) {
  if (!translations[lang]) return;
  currentLang = lang;
  localStorage.setItem('lang', lang);

  const isRtl = lang === 'ar';
  document.documentElement.lang = lang;
  document.documentElement.dir = isRtl ? 'rtl' : 'ltr';
  document.body.classList.toggle('lang-ar', isRtl);
  document.body.classList.toggle('lang-latin', !isRtl);

  document.title = t('meta.title');
  document.querySelector('meta[name="description"]').content = t('meta.description');

  document.querySelectorAll('[data-i18n]').forEach(el => {
    el.textContent = t(el.dataset.i18n);
  });

  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    el.placeholder = t(el.dataset.i18nPlaceholder);
  });

  document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.classList.toggle('active', btn.dataset.lang === lang);
  });

  if (typeof renderProjects === 'function' && (
    (typeof PROJECTS_DATA !== 'undefined' && PROJECTS_DATA.length) ||
    (typeof projects !== 'undefined' && projects.length)
  )) {
    renderProjects();
  }
  if (typeof renderReviews === 'function' && typeof reviews !== 'undefined' && reviews.length) {
    renderReviews();
  }
  if (typeof refreshOpenModal === 'function') refreshOpenModal();
  if (typeof refreshLightboxLabels === 'function') refreshLightboxLabels();
}

function initI18n() {
  document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.addEventListener('click', () => setLanguage(btn.dataset.lang));
  });
  setLanguage(currentLang);
}

function localized(field) {
  if (typeof field === 'string') return field;
  return field[currentLang] || field.en || Object.values(field)[0];
}
